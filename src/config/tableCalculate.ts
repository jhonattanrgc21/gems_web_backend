export function buscarIndexCalibre(array: any[], elem: any): number {
	/*
		Esta funcion retorna el indice del elemento que
		se este buscando. Retorna -1 si el elemento no existe
	*/

	let i = 0,
		n = array.length,
		encontrado = false,
		result: number;

	while (i < n && !encontrado) {
		if (array[i] == elem) encontrado = true;
		else i++;
	}

	if (encontrado) result = i;
	else result = -1;

	return result;
}

export function buscarTabla9(
	conductor: string,
	col: number,
	desfases: number,
): number {
	let index, result;

	index = buscarIndexCalibre(calibres9, conductor);
	if (index != -1) {
		if (desfases == 0) result = table9[col][index];
		else {
			index += desfases;
			if (index > calibres9.length) result = -1;
			else result = table9[col][index];
		}
	} else result = -1;

	return result;
}

